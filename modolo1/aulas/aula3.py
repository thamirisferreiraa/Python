entrada = input("Você gostaria de entrar ou sair? ")

if entrada == 'entrar':
    print('Bem vindo! Você entrou no sistema.')	
elif entrada == 'sair':
    print('Você saiu do sistema.')    
else:
    print('Você não escolheu nenhuma opção válida.')
    print('Obrigado por usar o sistema!')    
